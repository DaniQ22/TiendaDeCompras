    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cliente cliente = new Cliente();

        int nit = Integer.parseInt(this.NitCliente.getText());
        cliente.setIdentificacion(nit);
        this.NitCliente.setText("");

        String nombre = this.NombreCliente.getText();
        cliente.setNombre(nombre);
        this.NombreCliente.setText("");

        String ape = this.ApellidoCliente.getText();
        cliente.setApellido(ape);
        this.ApellidoCliente.setText("");

        tienda.registraCliente(cliente);
        JOptionPane.showMessageDialog(null, "El cliente ha sido registrado exitosamente!!");
    }//GEN-LAST:event_jButton1ActionPerformed

