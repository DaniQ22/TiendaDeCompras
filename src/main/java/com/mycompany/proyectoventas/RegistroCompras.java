    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Productos producto = new Productos();

        int codigo = Integer.parseInt(this.CodigoProducto.getText());
        producto.setCodigo(codigo);
        this.CodigoProducto.setText("");

        String nombreP = this.NombreProducto.getText();
        producto.setNombreProducto(nombreP);
        this.NombreProducto.setText("");

        int cantidad = Integer.parseInt(this.CantidadProducto.getText());
        producto.setCantidad(cantidad);
        this.CantidadProducto.setText("");

        double precio = Integer.parseInt(this.PrecioProducto.getText());
        producto.setPrecio(precio);
        this.PrecioProducto.setText("");

        tienda.registrarProducto(producto);
        JOptionPane.showMessageDialog(null, "El producto se ha registrado correctamente");


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        Cliente clie = tienda.buscarcliente(Integer.parseInt(this.RegisNit.getText()));
        Productos pro = tienda.buscarProducto(Integer.parseInt(this.RegiCo.getText()));
        int nit = 0;
        int codi = 0;
        int can = Integer.parseInt(this.RegiCan.getText());
        tienda.validarcantida(can);
        this.RegiCan.setText("");

        /* 
            if (tienda.compra[i].getProductos().getCantidad() < Integer.parseInt(this.RegiCan.getText())) {
                JOptionPane.showMessageDialog(null, "La cantidad es mayor a la cantidad registrada");
            } else {
                
            }
        }*/
        if (clie != null && pro != null) {

            nit = Integer.parseInt(this.RegisNit.getText());
            this.RegisNit.setText("");

            codi = Integer.parseInt(this.RegiCo.getText());
            this.RegiCo.setText("");

            tienda.RegistrarCompra(nit, codi, can);
            JOptionPane.showMessageDialog(null, "La compra se ha resgistrado correctamente");

        } else if (clie == null || pro == null) {
            JOptionPane.showMessageDialog(null, "El cliente o producto no existe");

        }


    }//GEN-LAST:event_jButton5ActionPerformed

